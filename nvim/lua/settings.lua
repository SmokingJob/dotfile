-- lua/settings.lua

-- Configuración básica
vim.opt.number = true            -- Numeración de líneas
vim.opt.relativenumber = true    -- Numeración relativa
vim.opt.tabstop = 4              -- Espacios por tabulación
vim.opt.shiftwidth = 4           -- Espacios para auto indentación
vim.opt.expandtab = true         -- Usar espacios en lugar de tabs
vim.opt.termguicolors = true     -- Habilitar colores en la terminal
vim.opt.splitright = true        -- Divisiones verticales a la derecha
vim.opt.splitbelow = true        -- Divisiones horizontales abajo
vim.opt.wrap = false             -- Desactivar ajuste de línea
vim.g.mapleader = ' '            -- Definir la tecla líder

